<h1>Reasons not to use Intel</h1>

<p>
Don't use Intel processors newer than Core2, because they have the
<a href="https://www.fsf.org/blogs/sysadmin/the-management-engine-an-attack-on-computer-users-freedom">
"management engine" back door</a>.
</p>

<p>
Recent AMD processors have a similar problem, but we do not yet have
an article about it.
