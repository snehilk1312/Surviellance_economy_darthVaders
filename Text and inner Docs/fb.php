to begin with.
<p>
This is not yet proof, but given so many other reasons to avoid Facebook,
why not take this precaution?

<hr />

<p>
Another study shows that being used a lot by Facebook
<a href="http://www.eurekalert.org/pub_releases/2015-04/uoh-usl040615.php">encourages depression</a>;
 since people generally post an exaggerated positive
picture of their lives, their lives appear to be better than your own.</p>

<hr />
<p>
Allowing yourself to be used frequently by Facebook
<a href="http://www.motherjones.com/mojo/2014/03/study-college-women-who-use-facebook-more-likely-have-eating-disorders">
promotes eating disorders</a>.
</p>

<hr />
<p>
  Facebook (along with Twitter and Snapchat)
  <a href="https://www.vice.com/en_us/article/the-secret-ways-social-media-is-built-for-addiction">
  uses tricks</a>
  to make people feel anxious and spend time there.
</p>

