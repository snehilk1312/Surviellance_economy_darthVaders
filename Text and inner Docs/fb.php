<p>Social networks, for lonely people, may only show them
<a href="http://www.guardian.co.uk/media/2010/aug/07/social-networking-friends-lonely">how lonely they are</a>.

<p>
A study found that being heavily used by Facebook
<a href="http://abcnews.go.com/Technology/facebook-making-sad-/print?id=19975348">tended
