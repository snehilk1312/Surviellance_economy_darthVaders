<h2 id="censorship">Censorship</h2>

<h3>Political Censorship</h3>

<p>
Facebook has frequently removed postings about protests (both planned
and ongoing), political satire, and various political issues.
Specific examples are given below.
</p>

<hr />

<p>
Facebook has <a href="http://www.bbc.com/news/technology-43070555">imposed political censorship</a> on Instagram, bowing to
Russia.</p>

<hr />
<p>
Israel bullied Facebook into imposing
<a href="https://theintercept.com/2017/12/30/facebook-says-it-is-deleting-accounts-at-the-direction-of-the-u-s-and-israeli-governments/">tight
censorship on criticism of the occupation of Palestine</a>.</p>

<hr />
<p>
Facebook deleted the news item announcing a
<a href="http://www.march-against-monsanto.com/facebook-censored-and-deleted-march-against-monsanto-event">
major protest against
Monsanto</a>.

<hr />

<p>
Facebook
<a href="http://www.nytimes.com/2014/12/23/world/europe/facebook-angers-russian-opposition-by-blocking-protest-page.html?partner=rss&emc=rss">
blocked a page announcing a protest</a> in Russia obeying orders
from the Russian government.
</p>
<p>
The order says the protest is illegal.  In a tyrannical state,
protests are
generally illegal.
</p>

<p>
Facebook has yielded to Turkey's
<a href="http://www.theguardian.com/technology/2015/jan/27/facebook-blocks-turkish-page-insults-prophet-muhammad">religious
censorship</a>, just as previously it yielded to China's
<a href="https://stallman.org/archives/2011-mar-jun.html#01_May_2011_(Urgent:_Urge_Facebook_not_to_partner_with_censors)">political
censorship</a>.
</p>

<hr />

Facebook has developed
<a href="https://www.theguardian.com/technology/2016/nov/23/facebook-secret-software-censor-user-posts-china">software
to allow various countries to directly control censorship</a> of what
useds in that country can see.

<hr />
<p>
Jim Wright forcefully condemned the pressure put on Americans to
