	<h1>Reasons not to use LinkedIn</h1>

	<ul>

	  <li id="scraped">
	    LinkedIn is trying to have people
	    <a href="https://www.eff.org/deeplinks/2017/12/eff-court-accessing-publicly-available-information-internet-not-crime">imprisoned
	    for scraping</a> its site.
	  </li>
	  <li>
	    LinkedIn requires you to use
	    <a href="https://www.gnu.org/philosophy/javascript-trap.en.html">non-free
	      Javascript</a> to access the site.
	  </li>

	  <li>
	    LinkedIn
	    <a href="https://www.theguardian.com/technology/2014/jun/04/linkedin-tiananmen-posts-china-censorship">implements
	      censorship</a> on behalf of the repressive Chinese state.
	  </li>

	  <li>
	    LinkedIn makes it "near impossible" to sign up without
	    <a href="https://medium.com/@danrschlosser/linkedin-dark-patterns-3ae726fe1462">giving
	      the company your address book</a>.
	    <p>
	      Perhaps this is effective only for users of the most common
	      email services, but that doesn't make it any less nasty.
	    </p>
	  </li>

          <li>
	    <p>
	    LinkedIn claims to own every user's profile, and puts
	    <a href="http://www.hrexaminer.com/why-i-killed-my-linkedin-account/">self-serving
	    restrictions on what information users can put in the
	    profile</a>.  Not to mention the dangerous indemnity
	    requirement.</p>
	    <p>
	    Ignore the weak ending that advises people not to stand on
	    principle.  We can strengthen others, and strengthen
	    principle, by encouraging them to take a stand on
	    principle (and setting examples ourselves).</p>
	  </li>

	  <li>
	    <p>
	    A paying customer of LinkedIn closed his account, feeling
	    that LinkedIn had started cheating him by eliminating the
	    features he wanted to pay for.  Then LinkedIn
	    <a href="https://medium.com/@martingeddes/linkedin-or-lockedin-why-i-deleted-my-account-and-maybe-you-should-too-8cad40a0ea68">refused
	    to delete his account immediately</a>.</p>
	    <p>
	    One of the features he resented losing, being informed of
	    what users said when referring others to his account,
	    seems like an injustice to me.  But his overall conclusion
	    &mdash; that we must manage our own digital identities,
	    not entrust that to some company &mdash; is right.</p>
	  </li>
	</ul>

