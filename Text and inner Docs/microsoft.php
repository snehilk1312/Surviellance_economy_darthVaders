	<h1>Reasons not to use Microsoft</h1>

	<ul>

	  <li>
	    Microsoft is running
	    <a href="http://techrights.org/2017/02/27/microsoft-novell-v2-via-azure/">a
	    patent protection racket</a>, threatening to sue users of
	    free software.
	  </li>

	    <li>
		Microsoft's principal wrong is distributing a
		<a href="http://gnu.org/philosophy/free-software-even-more-important.html">nonfree operating
		    system</a>, Microsoft Windows.
		<br />
		    That system is jam-packed with
		    <a href="http://gnu.org/proprietary/malware-microsoft.html">malicious functionalities</a>,
		    including surveillance of users, DRM, censorship and
		    a universal back door.
	    </li>

	    <li>
		Microsoft
		<a href="http://www.theguardian.com/technology/2016/feb/02/microsoft-downloading-windows-10-automatic-update">tricked
		    users</a> into "upgrading" to Windows 10.
	    </li>
	    <li>
	    Microsoft Windows 10 <a href="https://www.cnet.com/news/microsoft-windows-10-forced-updates-auto-restarts-are-the-worst/">forced software changes can sabotage the user terribly</a> if Microsoft chooses an inconvenient time to do them. Since the article is in the mainstream media, it suggests only to buy another computer that serves a master that doesn't do this particular form of nastiness. It completely ignores the possibility of installing a free operating system in the PC&mdash;which doesn't even require buying a new computer.
	    </li>
	    <li>
		Microsoft tablets and phones impose
		<a href="http://www.itworld.com/article/2832657/operating-systems/microsoft-metro-app-store-lock-down.html">censorship
		    of applications</a>.
	    </li>

	    <li>
		Microsoft's chatbot in China threatens people who communicate
		<a href="http://chinadigitaltimes.net/2016/11/microsofts-chinese-chatbot-encounters-sensitive-words/">using
		    prohibited words</a>.
	    </li>

	    <li>
		Microsoft
		<a href="http://noooxml.wikidot.com/">forced a ridiculous</a> "open"
		standard, OOXML (used in DOCX files), through the International
		Standards Organization by corrupting most of the national
		standards organizations that voted.
		<br />
		The specifications document was so long that it would be difficult for
		anyone else to implement it properly.  When the proposed standard was
		submitted through the usual track, experienced evaluators rejected it
		for many good reasons.
		<br />
		Microsoft responded using a special override procedure in which its
		money buy the support of many of the voting countries, thus bypassing
		proper evaluation and demonstrating that ISO can be bought.
	    </li>

	    <li>
		Microsoft pressured nearly all manufacturers of PCs to pay for a
		Windows license for every machine sold,
		<a href="http://www.linfo.org/microsoft_tax.html">thus charging every
		    purchaser for a Windows license</a>.
		<br />
		This is referred to sardonically as the "Microsoft tax".
		<br />
		(Wherever that page says "Linux" it actually means the
		<a href="http://gnu.org/gnu/linux-and-gnu.html">GNU/Linux</a>
		operating system rather than Linux proper.)
		<br />
		The fee doesn't force you to run Windows on your PC, but it is an
		injustice nonetheless.  One way to avoid it is to buy hardware
		that is
		<a href="http://fsf.org/resources/hw">never sold with Windows</a>.
		<br />
		Some countries have laws under which users have sued for the right to
		get a reimbursement for the Windows license.  Exercising that right is
		a hassle, but doing so is useful as it puts pressure on the system
		Microsoft has set up.  However, the existence of an inconvenient
		escape path, limited to a few countries, has no effect on the judgment
		that Microsoft's practices are an injustice and deserve condemnation.
	    </li>
	</ul>

