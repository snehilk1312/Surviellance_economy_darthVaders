<p>
My last reports (Jan 2019) say it is possible to use Twitter
without running nonfree software, and possible to create an account
with an email address instead of a phone number.

<p>
However, the report said that it is impossible to create an account
without running
<a href="http://gnu.org/philosophy/javascript-trap.html">
nonfree Javascript software</a>.
</p>

<p>
  Please email me if you have newer information about this issue.
</p>

<hr />
<p>
  Twitter has implemented new non-privacy settings;
  <a href="https://www.eff.org/deeplinks/2017/05/how-opt-out-twitters-new-privacy-settings">the default
    is substantial surveillance</a>, if users don't turn it off.
</p>

<hr />

<p>
  <a href="https://theintercept.com/2017/02/20/how-to-run-a-rogue-government-twitter-account-with-an-anonymous-email-address-and-a-burner-phone/">Twitter makes it impossible to create an account without providing
  a mobile phone number.</a>  You might have to buy a "burner phone" to do
  it.
</p>
<p>
  That article provides advice for those who want to run a Twitter
  account and protect their identities from a state that represses
  whistleblowers.  But even if you will proudly put your real name on
  the account, you have every reason not to let Twitter know your mobile
  phone number.
</p>

<hr />
<p>
  Twitter shut down Jim Weber's account after he posted a
  <a href="https://www.linkedin.com/pulse/how-gif-aly-raismans-floor-routine-got-me-permanently-jim-weber">
    copy of a video from the Olympic Games</a>.
</p>
<p>
  A social network should work for its users, which means it should not
  act as an instrument for someone else to control or restrict them.
  That means it should not enter the business of publishing on behalf of
  moneyed interests.
</p>
<p>
  It is too bad that Weber legitimized the power that was used against
  him by referring to these videos as "illegal".
</p>

<hr />
<p>
  Roy Schestowitz reports being
  <a href="http://schestowitz.com/Weblog/archives/2017/03/18/censored-by-twitter-for-mentioning-being-censored-by-twitter/">censored
  ("shadowbanned") by Twitter</a> for posting about Twitter
  censorship.
</p>
<p>
  This kind of censorship is not obvious to the one being
  censored.
</p>

