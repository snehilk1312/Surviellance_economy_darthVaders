<h2 id ="privacy">Privacy</h2>
<ul>

  <li>
    Uber collects data about passengers, then
    <a href="http://gizmodo.com/uber-doesn-t-want-you-to-see-this-document-about-its-va-1795151637">uses
    the data to profile them in various ways</a>.
  </li>

  <li>
    Uber requires passengers to identify themselves, both to order a
    ride and to pay.  (Uber
    offers <a href="https://newsroom.uber.com/macau/cash-eng/">some
    users in a few places</a> the option of paying cash, but they are
    still tracked and identified when ordering the ride.)
  </li>

  <li>
    It also records where you get the ride and where you go with
    it.
  </li>

  <li>
    Uber can track who has a one-night stand.
    <a href="http://www.marketplace.org/topics/business/final-note/ubers-data-makes-creepy-point-about-company">
      In fact, it did so.</a>
  </li>

  <li>
    Uber has started tracking users' movements
    <a href="https://techcrunch.com/2016/11/28/uber-background-location-data-collection/">before and after rides</a>.
    <p>
      In August 2017, Uber
      <a href="https://www.theguardian.com/technology/2017/aug/29/uber-u-turn-tracking-users-after-trip-ended-app-user-privacy-new-ceo">made
      it possible to turn that extra tracking off</a>, but it is still
      enabled by default, and most customers won't think to turn it
      off.
    </p>
    <p>
      That is only a tiny step forward.
    </p>
  </li>

    <a href="https://www.theguardian.com/money/2016/apr/22/uber-scam-hacking-account-phantom-journeys">people
      whose accounts have been stolen</a>.
  </li>

  <li>
    <p>
    Uber hacked Lyft's ride-management server with phony accounts in
    order to
    <a href="https://www.engadget.com/2017/04/13/uber-hell-program-lyft-drivers/">identify
    people that were driving for Lyft</a>.</p>
  </li>

  <li>Uber <a href="http://www.theguardian.com/technology/2015/jun/22/uber-ftc-privacy-customer-location-contacts">plans to snoop</a> on users' locations and contacts all the time.
    <p>
      Uber has the technical possibility to do this because its app is nonfree:
      it is controlled by Uber, not by the user.  In addition, snooping depends
      on a nonfree operating system.  With a free system, the user could tell
      the system to lie to the Uber app.
  </li>

  <li>
    The <a
	   href="http://gnu.org/philosophy/surveillance-vs-democracy.html">US
      government can get those records</a>, and any lawsuit (such as a
    divorce lawsuit) can subpoena them.
  </li>

  <li>
    Uber
    <a href="http://www.huffingtonpost.com/entry/uber-customer-data-privacy_us_570e518ae4b0ffa5937da329">
      gave the US government data</a> on millions of customers.
  </li>

  <li>
    Uber's clever policy of not being directly responsible for anything
    that goes wrong extends to harassment by drivers, and its practice of
    identifying passengers enables drivers to find out who the passenger is.
    This makes some women <a
			     href="http://www.thedailybeast.com/articles/2014/03/28/uber-s-biggest-problem-isn-t-surge-pricing-what-if-it-s-sexual-harassment-by-drivers.html">
      scared to use Uber</a>.
    <p>
      This problem comes directly out of the practices listed above that mistreat
      all users of Uber.
  </li>

  <li>
    Uber
    <a href="http://www.theguardian.com/technology/2014/nov/19/uber-investigates-top-executive-after-journalists-privacy-was-breached">
      executives and staff have stalked passengers</a> in various ways.
    <p>
      If you take an ordinary taxi and pay cash, it will generate no records
      associated with you &mdash;
      <a href="https://stallman.org/archives/2012-may-aug.html#10_August_2012_%28New_surveillance_system_in_New_York%29">
	except in New York City</a> where the
      government
      might apply face recognition to identify your photo in real time.
  </li>

  <li>
    <p>
      To recover our privacy and <a
				    href="http://gnu.org/philosophy/surveillance-vs-democracy.html">make
	democracy safe</a>, we need to redesign digital systems so that they
      do not collect information about people in general.  First step, don't
      help any new ones gain a foothold.
    </p>
  </li>

  <li>
  <p><a href="https://therideshareguy.com/uber-is-ripping-off-frequent-riders-and-heres-how-to-avoid-it/">It appears that Guber gouges frequent or regular passengers</a> by charging them more.</p>
  <p>If we eliminate the surveillance injustice of Guber by stopping it from identifying passengers, this wrong would automatically go away too.</p>
  </li>
</ul>

<h2 id ="usersfreedom">Users' Freedom</h2>
<ul>
  <li>Uber requires customers to run a nonfree program (an app).
    As always, <a href="http://gnu.org/philosophy/free-software-even-more-important.html">a nonfree program tramples its users' freedom</a>.
    <p>
      I'm not talking about the software that Uber runs in its
      servers; that does not directly affect customers.  If some of
      that software is nonfree, it tramples Uber's freedom, but not
      the customers' freedom.  The nonfree software and digital
      services that Uber requires <em>its users</em> to use
      attack <em>their</em> freedom in various ways.
  </li>

  <li>
    The Uber app requires running other nonfree software (in the case of
    Android, <a
		href="http://gnu.org/philosophy/android-and-users-freedom.html">
      Google Play</a>).
  </li>

  <li>
    Uber is trying again to
    <a href="http://www.newyorkpersonalinjuryattorneyblog.com/2016/12/opting-out-of-ubers-forced-arbitration-the-clock-is-ticking.html">force
      all customers to agree to arbitration</a>
    of disputes &mdash; rather than lawsuits.
  </li>

</ul>

<h2 id ="abuse">Abuse of Drivers</h2>
<ul>

  <li>
    Uber and Lyft Drivers Say Apps Are <a href="https://www.theguardian.com/technology/2019/apr/18/uber-lyft-drivers-surge-pricing-wages">Short-Changing Wages While Raising Fares</a>.
  </li>
  <li>
    Guber drivers are suing Guber, accusing it of
    <a href="https://arstechnica.com/tech-policy/2017/04/uber-said-to-use-sophisticated-software-to-defraud-drivers-passengers/">programming
    its software to cheat them and passengers through
    misinformation</a>.
  </li>

  <li>
    Uber does not allow drivers to sue &mdash; it requires them to use
    arbitration for disputes with the company.  But they can't
    actually use arbitration because Uber
    <a href="https://www.reuters.com/article/legal-us-otc-uber/forced-into-arbitration-12500-drivers-claim-uber-wont-pay-fees-to-launch-cases-idUSKBN1O52C6">won't
    let them start cases</a>.</p>
    <p>
    We should not allow companies to impose a requirement for
    arbitration rather than a lawsuit, not on customers and not on
    workers.</p>
  </li>

  <li>
    <a href="https://www.nytimes.com/interactive/2017/04/02/technology/uber-drivers-psychological-tricks.html">Uber
    uses psychological tricks</a> to manipulate drivers to do things
    that are against their interests, such as spend more time driving
    around unpaid.
  </li>

  <li>
    Uber has
    <a href="https://www.theguardian.com/technology/2017/may/23/uber-underpaid-drivers-new-york-city">repeatedly
    underpaid its drivers</a>.
  </li>

  <li>
    Some Uber drivers
    <a href="https://www.theguardian.com/technology/2019/may/07/the-uber-drivers-forced-to-sleep-in-parking-lots-to-make-a-decent-living">have
    to sleep in their cars to work in San Francisco</a>.  They can't
    afford to live near that city, on Guber's measly pay.
  </li>

  <li>
    Uber led drivers in India into taking on debt to buy cars, them
    cut their pay so that they now have to work
    <a href="https://www.theguardian.com/global-development/2018/dec/04/my-life-is-spent-in-this-car-uber-drives-indian-workers-to-despair">over
    12 hours every day</a>.
  </li>

  <li>
    Women working at Uber encounter so much
    <a href="https://www.recode.net/2017/2/19/14665076/ubers-travis-kalanick-susan-fowler-sexual-harassment-investigation">harassment</a>
    that it chases them out.
  </li>

  <li>
    <a href="https://www.theguardian.com/commentisfree/2017/mar/03/uber-spreading-social-poison-travis-kalanick">The
      CEO of Guber insulted drivers</a>, saying it was their fault that
    they went bankrupt, not Guber's fault for paying them peanuts.
  </li>

  <li>
    Uber is
    <a href="http://www.thestranger.com/slog/2017/01/27/24830634/uber-is-asking-drivers-to-send-them-non-uber-paystubs-for-money">pressing
    drivers to disclose what they are paid
      by competitors</a> of Uber (drivers often work for more than one
    company).
  </li>

  <li>
    Uber pretends it only operates a marketplace, but in fact it
    <a href="http://www.slate.com/articles/technology/future_tense/2015/07/uber_s_algorithm_and_the_mirage_of_the_marketplace.single.html">manipulates
      the marketplace</a>.  It shows drivers only a massaged version of actual
    demand.
  </li>

  <li>
    Uber pays drivers peanuts; we should call it "Goober", or rather "Guber".
    <p>
      Guber is an
      <a href="http://valleywag.gawker.com/uber-cuts-prices-in-new-york-and-fares-for-drivers-1601142883">
	unregulated near-monopoly</a>, so it can cut rates for drivers
      arbitrarily.
    <p>
      <a href="http://www.washingtonpost.com/local/trafficandcommuting/some-uber-drivers-say-companys-promise-of-big-pay-day-doesnt-match-reality/2014/09/06/17f5d82c-224a-11e4-958c-268a320a60ce_story.html">
	Drivers are starting to complain</a> that they're left with little money
      for their work.
    <p>
      GUber can arbitrarily cut drivers' pay,
      and they need to work 15 hours a day.  <a href="https://www.jacobinmag.com/2014/09/against-sharing/">Some are trying to unionize,</a> but it won't be easy.
  </li>

  <li>
    The low wages paid by Uber and Lyft have <a href="https://www.commondreams.org/views/2018/02/09/uber-and-lyft-driving-drivers-poverty-and-despair">driven some cab and limo
      drivers to suicide</a>.
  </li>
  <li>
    Uber and Lyft blame state governments for eliminating cities'
    power to
    regulate those companies.
  </li>
  <li>
    <p>
      Guber has organized in NYC something reminiscent of a company
      union for its drivers, except it
      <a href="http://www.labornotes.org/blogs/2016/08/putting-con-gig-economy">isn't
      even a union</a>.
  </li>

  <li>
    <a href="https://www.buzzfeed.com/carolineodonovan/internal-uber-driver-pay-numbers">Internal
      data from Guber</a> show that it really pays drivers peanuts.
  </li>

  <li>
    <p>
      Uber claims its drivers are independent contractors, but
      not very independent, since they are
      <a href="http://www.recode.net/2016/11/28/13768756/uber-driver-deactivation-juno-advertising-new-york">not
      allowed to contract for other ride companies</a>.
    </p>
    <p>
      By the way, it is clear that Juno has the same basic
      injustice as Uber: customers must run nonfree software
      and identify themselves.  Uber is more nasty and
      arrogant, but both of them are enemies of our
      freedom.  We should should reject both of them
      completely.
    </p>
  </li>

  <li>
    <p>
      Uber drivers in the US get a median net income of around $15 per
      hour (as of 2018).  But when the costs of driving are deducted,
      it falls
      to <a href="https://www.ridester.com/2018-survey/">under
      $10</a>.
    </p>
  </li>

    <li>
        <p>
        <a href="https://www.theguardian.com/technology/2018/mar/07/ubers-underpayment-of-drivers-keeping-it-afloat-report-finds">Uber's Underpayment of Drivers Keeping It Afloat,</a> Report Finds.
        </p>
        <p>
        We can protect our freedom by protecting drivers' pay rates.
        </p>
    </li>
</ul>

<h2 id ="taxes">Taxes</h2>
<ul>

  <li>
    Uber is selling low and losing money <a href="http://www.theguardian.com/commentisfree/2016/jan/31/cheap-cab-ride-uber-true-cost-google-wealth-taxation">to destroy its competitors.</a>
    <p>If you are a short-term thinker, you will be delighted to take
    advantage of this &mdash; until it controls the market and
    squeezes you forever.
  </li>

  <li>
    Uber
    <a href="http://www.bloomberg.com/news/articles/2016-04-06/the-sharing-economy-doesn-t-share-the-wealth">
      has already made arrangements</a> not to pay US taxes if it does
    start making a profit.
  </li>

  <li>
    Uber in China
    <a href="http://fusion.net/story/150389/uber-is-using-gps-to-punish-drivers-in-china-who-get-too-close-to-protests/">tracks
      drivers and actively pressures them to stay away from protests</a>.
    <p>
      The company could not prevent Chinese repression, but upholding it
      in this way is complicity.
  </li>

</ul>

<h2 id ="taxis">Comparison to Real Taxis</h2>
<ul>

  <li>
    When taxi drivers struck to protest the troll's partial Muslim
    ban,
    <a href="https://www.nytimes.com/2017/01/29/technology/silicon-valleys-ambivalence-toward-trump-turns-to-anger.html?partner=rss&emc=rss&_r=0">Uber
    took the opportunity to undercut the strike</a>.
  </li>

  <li>
    Cities and even public service campaigns are outsourcing
    transportation to Uber, which is a form of
    <a href="http://www.truth-out.org/news/item/38725-uber-and-lyft-are-threatening-to-expose-poor-and-elderly-to-predatory-practices">privatization of public transit</a>.
    This excludes poor people, and subjects those who do use the service
    to being tracked.
    <p>
      Lyft is no better than Uber in this regard.
  </li>


  <li>
    Uber has
    <a href="https://www.theguardian.com/technology/2016/apr/29/uber-penalty-fees-late-passengers">changed
      the regulations</a> that cover charging passengers for making cars wait.
    <p>
      This decision itself may not be objectionable. Taxis typically charge
      for making them wait. But that regulation is set by a city agency
      which is at least somewhat responsible to the people. Uber is a
      business headquartered somewhere else, which accepts no responsibility
      to the people of any city.
    <p>
      We should not allow a company to privatize the making of the
      regulations that create our social order.
  </li>

  <li>
      Uber
      <a href="http://www.theverge.com/2014/5/28/5758734/uber-will-eventually-replace-all-its-drivers-with-self-driving-cars">plans
	to do away with human cab drivers</a>.
      <p>
	It would be easy for a non-plutocratic government to prohibit this,
	and that's what every country ought to do, unless/until every person
	gets an adequate basic income so people don't need to be employed.
  </li>

  <li>
    <p>
      With real taxis, you can flag one on the street or phone in any
      fashion; you can pay cash; you can be anonymous.
    </p>
  </li>

  <li>
    <p>
      Beware of thinking of Uber as one more option in addition to real
      taxis.  At the moment, that's true, but if Uber is a big success,
      <a href="http://boingboing.net/2014/06/30/ubervalued-the-appropriation.html">
	real taxis could disappear</a>.
    </p>

    <p>
      Then what will you do, if you don't want to tell Big Brother where you
      are going?
    </p>
  </li>

</ul>

<h2 id ="discrimination">Discrimination</h2>
<ul>
  <li>
    <p>
    Uber has set up a social credit system: passengers that get bad
    ratings from drivers will be
    <a href="https://www.theguardian.com/technology/2019/may/31/uber-to-ban-riders-with-low-ratings">blacklisted</a>
    (refused disservice).</p>
    <p>
    One must suspect that customers' racial and ethnic appearance
    influences the ratings, so one effect will be discrimination.
    However, the chinification will do wrong to every customer,
    including those who are blacklisted, and those who only face the
    possibility of being blacklisted.</p>
    <p>
    This reinforces my conclusion that transportation services should
    not be allowed to identify their customers.</p>
  </li>
  <li>
    <a href="https://www.theguardian.com/technology/2016/oct/31/uber-lyft-racial-discrimination-us-boston-seattle">
    Uber systematically discriminates against passengers that are black,
    and tends to cheat women.
    </a>
    <p>
    The study does not demonstrate how the discrimination and cheating occur,
    but does demonstrate the statistical phenomenon.
  </li>

  <li>
    <p>
      Uber <a href="https://www.theguardian.com/commentisfree/2018/apr/13/uber-lyft-prices-personalized-data">charges different prices</a> to different people.  Other companies
      also engage, or have engaged, in price discrimination.  It turns out
      that identifying customers helps many businesses put customers at a
      disadvantage.
    </p>
    <p>
      The only thorough solution is to do what is needed for other reasons:
      eliminate the systems that let companies know who the customer is.
    </p>
  </li>

</ul>

<h2 id ="legal">Legal</h2>
<ul>
  <li>
    <a href="http://www.theverge.com/2016/7/10/12127638/uber-ergo-investigation-lawsuit-fraud-travis-\
kalanick">
      Uber investigated a lawyer</a> handling a lawsuit against Uber's policy of
    surge pricing, using methods that may reach the domain of illegality.
  </li>

 <li>
    <p>
      Guber programmed its system for special treatment of people that
      might be investigating accusations of violating municipal
      regulations.  The special treatment was that the system would
      <a href="https://www.theguardian.com/technology/2017/mar/03/uber-secret-program-greyball-resign\
ation-ed-baker">say
      a car was coming, but no car would ever arrive</a>.
    </p>
    <p>
      Uber has the ability to do this because it forces all its
      clients to identify themselves &mdash; which is fundamentally
      unjust.
    </p>
  </li>

  <li>
    <p>
    Uber's autonomous cars
    <a href="https://www.theguardian.com/technology/2016/dec/16/uber-self-driving-cars-california-ill\
egal-unethical-tactics">frequently
    violate traffic laws.</p>
    <p>
    Uber
    <a href="https://www.cnet.com/news/uber-we-dont-need-a-permit-for-self-driving-cars/">tries
    to put the blame on human safety monitors</a>, who could in
    principle intervene to stop this.</p>
    <p>
    That's the general Uber attitude: profits to the company, burdens
    to the drivers.</p>
    <p>
    I would guess that the safety monitors would be hard-pressed to
    react fast enough to prevent these maneuvers &mdash; which would
    mean that they are an excuse and a scapegoat, not a real safety
    system.</p>
  </li>

  <li>
    Uber operated a team to <a href="https://www.theguardian.com/technology/2017/nov/28/uber-court-waymo-trade-secrets-trial">spy on competitors</a> and to "impede, obstruct or
    influence" legal investigations, according to an ex-manager who was
    involved with the team.
  </li>

  <li>
    Uber blames state governments for eliminating cities' power to
    regulate such companies.
  </li>

  <li>
    <p>
    Uber imposes arbitration on customers
    <a href="https://www.theguardian.com/technology/2018/mar/15/uber-class-action-lawsuit-sexual-assault-rape-arbitration">
    for all complaints, including rape by drivers,
    </a>
    and this covers up the prevalence of the problem.
    </p>
    <p>
    No company should be allowed to require its customers or its workers
    (whether labeled "employee" or "independent contractors") to use
    arbitration instead of going to court.
    </p>
  </li>

</ul>

