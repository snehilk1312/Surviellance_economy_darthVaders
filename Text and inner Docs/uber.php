<h1>Reasons not to use Uber</h1>

<blockquote>
We should not accept the promotional term "sharing economy" for
companies like Uber.  That is spin.  A more accurate term is
"piecework subcontractor economy".
</blockquote>

<p>
Uber is a big advance in massive surveillance.  Unlike a taxi,
an Uber car can't be used anonymously.  You can't pay cash.
It also requires use of nonfree software, which mistreats the user.
</p>

<p>
  Uber <a href="https://usa.streetsblog.org/2019/02/04/all-the-bad-things-about-uber-and-lyft-in-one-simple-list/">increases
    car traffic</a>, increase wasteful driving,
  reduce use of other transit modalities, and undermine public transit.
</p>

<p>
Because I reject technology that mistreats me, I will never order or
pay for an Uber car.  Because Uber threatens to eliminate the taxis
that are acceptable to use, I go beyond that: I will not let someone
order an Uber car for me.  How about joining me?
</p>

