        <h1>Reasons not to buy from Amazon</h1>

        <p>
            If you want to order a book (or something else), don't buy it from
            Amazon.  Amazon harms its customers, as well as workers,
	    the national treasury, and many others that it affects.
	</p>

        <p>
            Here's a good (though long)
            <a href="http://www.theguardian.com/technology/2013/dec/01/week-amazon-insider-feature-treatment-employees-work">
            overview of why Amazon's overall activity
            is harmful</a> to society overall.
        </p>

	<p>
	  This page lists
	  <a href="http://www.ethicalconsumer.org/boycotts/boycottamazon/amazonshoppingalternatives.aspx">alternatives
	  to Amazon</a> for buying various kinds of products.  Some of
	  these sites may share some of Amazon's unethical practices.
	  I am pretty sure that any site
	  <a href="/internet-music.html">selling MP3 files</a> on the
	  internet imposes an EULA &mdash; an inexcusable wrong.
	  Streaming sites, too.  And all of them identify the
	  purchaser.  It is better to buy from a store, and pay cash.
	  Or else get a copy through sharing.
	</p>
	<p>
	  For a book, order it directly from the publisher or
          through a local book store.  If you want to use a URL to
          refer to a book, please don't use an Amazon page.
        </p>
