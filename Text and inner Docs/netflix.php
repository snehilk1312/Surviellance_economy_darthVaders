<h1 id="heading">Reasons not to use Netflix</h1>
<div id="wrap">

<p>
  Netflix commits the three usual injustices of digital
  publishing: <a href="http://DefectiveByDesign.org">Digital
    Restrictions Management (DRM)</a>, End User License Agreements that
  restrict the customer beyond the restrictions of copyright law, and
  identifying and tracking the customers.
</p>
<p>
To tolerate any of these predatious practices is to surrender our
rights without a fight.  Please join me in refusing: Flick off
Netflix.
</p>

<ul id="boxes">

<li>
<p>
  Netflix is a surveillance system: it demands customers identify
  themselves to pay, and thus records what each person watches.
  Protect your privacy &mdash; reject Netflix!
  </p>
</li>

<li>
  <p>
  Netflix is a typical streaming media dis-service: it requires a
  nonfree client program that imposes digital restrictions mechanisms
  (<a href="http://DefectiveByDesign.org/">DRM</a>) intended to stop
  users from using the data sent by Netflix other than through nonfree
  software designed to restrict what users do with it.  For your
  freedom's sake, never use DRM that you can't break, so you should
  not use these dis-services unless you can break their DRM.
  </p><p>
  A friend once asked me to watch a video with her that she was going
  to display on her computer using Netflix.  I declined, saying that
  Netflix was such a threat to freedom that I felt uncomfortable
  with legitimizing it in this way.
  </p><p>
  Rejecting streaming DRM is an ethical imperative because this
  streaming technology is intended to divide people and make them antisocial.
  </p>
</li>

<li>
<p>
  Netflix makes customers sign a restrictive antisocialization contract
  (usually called an "End User License Agreement", meaning that users
  are compelled to agree) that denies them rights that they would
  normally have under copyright law if they bought a copy.  If you
  bought a copy on a tape or disk, you would be free <em>even under
  copyright law</em> to give, lend or resell it.  With Netflix, those
  actions are not only obstructed by DRM, but also explicitly
  forbidden.  Of course, these contracts typically also include a
  commitment not to share copies of the work, and that requirement
  is <a href="https://www.gnu.org/philosophy/copyright-vs-community.html">
  unjust also.</a>
<p>
  Educators speak the mission of "socialization" of children, meaning
  teaching them to be good, cooperating members of their community.
  That includes being willing in general to give things to others,
  lend things to others, and share copies with others.  This contract
  aims to do the exact opposite, so we call it "antisocialization".
  More informally, I call it "jerkogenic", because it makes people
  promise to act like jerks.
<p>
  If you have agreed to a contract to be an antisocial jerk, that is no
  excuse to <em>be</am> an antisocial jerk.  Therefore, if you have
  clicked "I agree" on such a contract, you must not carry out the
  antisocial commitment.
<p>
  However, since I would feel ashamed to have agreed to an agreement I
  know I would be morally bound to break, I decline to agree to them.
  This is why I will never use Netflix unless it changes drastically.
  Please join me in refusing to agree to Netflix's antisocialization
  contract, or others of the same nature.
  </p>
</li>

<li>
  <p>
    Netflix
    has <a href="https://torrentfreak.com/inside-the-mpaa-netflix-amazon-global-anti-piracy-alliance-170918/">joined
    with the MPAA</a> to campaign for repression of sharing on the
    net.
  </p>
</li>

<li>
Netflix is parlaying its control of users (obtained through the wrongs
stated above) into
<a href="http://www.theguardian.com/media/2016/jan/07/netflix-streaming-ces-global-tv-network">
a grab for control over video entertainment</a>.
<p>
Every time you hear that some video is produced specifically by
Netflix, it is appropriate to feel a wave of disgust and
resentment.  Those feelings will help you refuse to watch it &mdash;
refuse to feed the monster.
</li>

<li>
<p>
Netflix in cahoots with the movie companies is
<a href="http://torrentfreak.com/netflix-cracks-down-on-vpn-and-proxy-pirates-150103/">attacking
users' privacy by blocking access through VPNs</a>.</p>
</li>

</ul> <!-- END boxes -->
