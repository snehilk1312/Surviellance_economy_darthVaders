<h1>Spotify</h1>

<p>
Like most "music screaming" dis-services, Spotify is based on
proprietary malware (DRM and snooping).  Recently it
<a href="http://www.theguardian.com/technology/2015/aug/21/spotify-faces-user-backlash-over-new-privacy-policy">
demanded users
submit to increased snooping</a>, and some are starting to realize that it
is nasty.
</p>
<p>
However, it was nasty all along.  <em>Any</em> "service" that is based
on discouraging or impeding people from having their own copies is a
scheme to interfere with sharing.
</p>
<p>
<a href="https://web.archive.org/web/20160313214751/http://www.theregister.co.uk/2015/08/21/spotify_worse_than_the_nsa/">
This article shows the twisted ways</a> that they present snooping as a
way to serve users better
&mdash; never mind whether they want that.  This is a typical
example of the attitude of the proprietary software industry
towards those they have subjugated.
</p>

<hr />
<p>
Spotify
<a href="https://www.theguardian.com/commentisfree/2018/sep/16/spotify-can-tell-if-youre-sad-heres-why-that-should-scare-you">can
tell whether users are happy or sad, based on what they listen to</a>.
It will use that data to manipulate them.  The data will sooner or
later be combined with other personal data, to control people
according to the corporate will.</p>
<p>
To protect yourself from this tracking, do as I do &mdash; get an
actual copy of the song, and listen to it with free software.  Then no
company will know what you are listening to.</p>

<hr />

<p>
Out, out, damned Spotify!
</p>

